export function getTranslation(lang: Language, key: string, defaultValue: string = ''): string {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return defaultValue;
    }
  }
  
  return typeof value === 'string' ? value : defaultValue;
}

export function t(lang: Language, key: string): string {
  return getTranslation(lang, key, key);
}

export function getArrayTranslation(lang: Language, key: string): string[] {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return [];
    }
  }
  
  return Array.isArray(value) ? value : [];
}

export function getObjectTranslation(lang: Language, key: string): Record<string, any> {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return {};
    }
  }
  
  return typeof value === 'object' ? value : {};
}
